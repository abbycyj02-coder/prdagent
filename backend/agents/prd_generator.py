def generate_prd(session: SessionState) -> str:
    template_prd = generate_template_prd(session)
    if llm_enabled():
        try:
            llm_prd = _generate_llm_prd(session, template_prd)
            if llm_prd:
                return llm_prd
        except Exception as exc:
            return f"{template_prd}\n\n## LLM 调用说明\n\n真实 LLM 调用失败，已回退到模板生成。错误：{exc}"
    return template_prd
