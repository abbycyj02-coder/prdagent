from __future__ import annotations

from backend.models import SessionState
from backend.services.llm_service import chat_completion, llm_enabled


def _value(session: SessionState, key: str, fallback: str = "") -> str:
    value = session.answers.get(key, "").strip()
    return value if value else (fallback or f"请补充 {key} 信息")


def _header(session: SessionState, title: str) -> str:
    return f"""# {title}

## 1. 版本说明

| 字段 | 内容 |
|---|---|
| 文档版本 | v0.1 MVP 草稿 |
| 产品类型 | {session.product_type} / {session.sub_type} |
| 生成时间 | {session.updated_at} |
| 当前状态 | {session.status} |
| 完整度 | {session.completeness_score}/100 |

## 2. 原始需求

{session.user_input}
"""


